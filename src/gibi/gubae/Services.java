    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        User user = new User();
        TableModel tabelModel = jTable1.getModel();
        user.setServiceType(tabelModel.getValueAt(index,0).toString());
        int a = JOptionPane.showConfirmDialog(null,"Do you want to see individual list of students registerded in this service type ","Select",JOptionPane.YES_NO_OPTION);
        if(a==0)
        {
            
            individualServiceList(user);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

