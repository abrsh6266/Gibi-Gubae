   public void serviceTable2(DefaultTableModel d)
   {
       txtServiceName.setText("");
       txtViceName.setText("");
       btnBack.setEnabled(true);
       jTable1.setModel(d);
       validating();
       services.setVisible(true);
   }
    public  void validating()
    {
        if((txtServiceName.getText()).equals("")|(txtViceName.getText()).equals(""))
        {
            btnAdd.setEnabled(false);
        }
        else
        {
            btnAdd.setEnabled(true);
        }
            
    }
    public void serviceTable(DefaultTableModel d)
   {
       validating();
       btnBack.setEnabled(false);
       jTable1.setModel(d);
       services.setVisible(true);
   }
